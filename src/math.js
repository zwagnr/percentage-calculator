//Math Equations
export const xPercentOfY = (x, y) => (x / 100) * y;
export const xIsWhatPercentOfY = (x, y) => (x / y) * 100;
export const xIsYPercentOf = (x, y) => x / (y / 100);
export const percentIncrease = (x, y) => x * (1 + y / 100);
export const percentDecrease = (x, y) => x * (1 - y / 100);